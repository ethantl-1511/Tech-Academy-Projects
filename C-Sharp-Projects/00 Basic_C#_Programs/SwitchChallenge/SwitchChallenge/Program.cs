using System;

namespace SwitchChallenge
{
    class Program
    {
        static void Main()
        {
            int day = 4;
            switch (day)
            {
                case 1:
                    Console.WriteLine("Monday");
                    Console.ReadLine();
                    break;
                case 2:
                    Console.WriteLine("Tuesday");
                    Console.ReadLine();
                    break;
                case 3:
                    Console.WriteLine("Wednesday");
                    Console.ReadLine();
                    break;
                case 4:
                    Console.WriteLine("Thursday");
                    Console.ReadLine();
                    break;
                case 5:
                    Console.WriteLine("Friday");
                    Console.ReadLine();
                    break;
                case 6:
                    Console.WriteLine("Saturday");
                    Console.ReadLine();
                    break;
                case 7:
                    Console.WriteLine("Sunday");
                    Console.ReadLine();
                    break;
            }
        }
    }
}
