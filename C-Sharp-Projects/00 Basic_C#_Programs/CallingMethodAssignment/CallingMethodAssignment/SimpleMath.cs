using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MethodAssignment
{
    class SimpleMath
    {
        // Simple function for adding
        public static int Add(int num)
        {
            return (num + 100);
        }

        // Simple function for subtraction
        public static int Subtract(int num)
        {
            return (num - 100);
        }

        // Simple function for multiplication
        public static int Multiply(int num)
        {
            return (num * 100);
        }
    }
}
